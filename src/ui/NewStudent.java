    private void jbtnS_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnS_saveActionPerformed
       if(isInputValid()){
           
            String name = txtSname.getText();
            String motherName = txtS_mothername.getText();
            String fatherName = txtS_fathername.getText();
            String email = txtS_email.getText();
            String address = txtS_address.getText();
            String institue = txtS_institue.getText();
            String nbId = txtS_nid.getText();
            long mobile = Long.parseLong(txtS_mobile.getText());
            String roomNo =(String) cmdboxRoom.getSelectedItem();
            String status = "Living";
        
        try {
            Connection con = ConnectionProvider.getCon();
            
           PreparedStatement ps = con.prepareStatement("insert into student(student_name,mother_name,father_name,email,address,institue,n_bid,mobile_no,roomno,status) values(?,?,?,?,?,?,?,?,?,?)");
           ps.setString(1, name);
           ps.setString(2, motherName);
           ps.setString(3, fatherName);
           ps.setString(4, email);
           ps.setString(5, address);
           ps.setString(6, institue);
           ps.setString(7, nbId);
           ps.setLong(8, mobile);
           ps.setString(9, roomNo);
           ps.setString(10, status);
           ps.executeUpdate();
           
           PreparedStatement ps1 = con.prepareStatement("update room set RoomStatus='Booked' where Number =?");
           ps1.setString(1, roomNo);
           ps1.executeUpdate();
           
           JOptionPane.showMessageDialog(this,"Successfully Added.");
           clear();
          
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
           
           
           
       }
       else{
           JOptionPane.showMessageDialog(this,"All Information Required!!");
           
       }



        
         
    }//GEN-LAST:event_jbtnS_saveActionPerformed

