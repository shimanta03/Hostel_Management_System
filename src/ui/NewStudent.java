    public void roomNumber(){
        int i= 0;
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select *from room where Activate='Yes'and RoomStatus ='Not Booked'");
            while(rs.next()){
                i = 1;
                cmdboxRoom.addItem(rs.getString(1));
            }
            if(i==0){
                jbtnS_save.setVisible(false);
                JOptionPane.showMessageDialog(this,"All Rooms Are Already Booked!");
                setVisible(false);
            }
            
        } catch (Exception e) {
        }
    }
